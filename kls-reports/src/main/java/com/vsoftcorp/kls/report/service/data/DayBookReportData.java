package com.vsoftcorp.kls.report.service.data;

import java.math.BigDecimal;

public class DayBookReportData {

	private BigDecimal cashOpBal;

	private String accountNum;

	private BigDecimal cashPayment;

	private BigDecimal cashReceipt;

	private String name;

	private BigDecimal transferPayment;

	private BigDecimal transferReceipt;

	private String voucherNum;

	private String productName;

	public BigDecimal getCashOpBal() {
		return cashOpBal;
	}

	public void setCashOpBal(BigDecimal cashOpBal) {
		this.cashOpBal = cashOpBal;
	}

	public String getAccountNum() {
		return accountNum;
	}

	public void setAccountNum(String accountNum) {
		this.accountNum = accountNum;
	}

	public BigDecimal getCashPayment() {
		return cashPayment;
	}

	public void setCashPayment(BigDecimal cashPayment) {
		this.cashPayment = cashPayment;
	}

	public BigDecimal getCashReceipt() {
		return cashReceipt;
	}

	public void setCashReceipt(BigDecimal cashReceipt) {
		this.cashReceipt = cashReceipt;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BigDecimal getTransferPayment() {
		return transferPayment;
	}

	public void setTransferPayment(BigDecimal transferPayment) {
		this.transferPayment = transferPayment;
	}

	public BigDecimal getTransferReceipt() {
		return transferReceipt;
	}

	public void setTransferReceipt(BigDecimal transferReceipt) {
		this.transferReceipt = transferReceipt;
	}

	public String getVoucherNum() {
		return voucherNum;
	}

	public void setVoucherNum(String voucherNum) {
		this.voucherNum = voucherNum;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	@Override
	public String toString() {
		return "DayBookReportData [cashOpBal=" + cashOpBal + ", accountNum=" + accountNum + ", cashPayment="
				+ cashPayment + ", cashReceipt=" + cashReceipt + ", name=" + name + ", transferPayment="
				+ transferPayment + ", transferReceipt=" + transferReceipt + ", voucherNum=" + voucherNum
				+ ", productName=" + productName + "]";
	}

}
