package com.vsoftcorp.kls.report.service.data;

public class ScheduleReportData {
	
	private String memberNumber;
	
	private String memberName;
	
	private String locNumber;
	
	private String productId;
	
	private String productName;
	
	private String noOfAccounts;
	
	private String closingBalance;
	
	private String totalBalance;

	public String getMemberNumber() {
		return memberNumber;
	}

	public void setMemberNumber(String memberNumber) {
		this.memberNumber = memberNumber;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getLocNumber() {
		return locNumber;
	}

	public void setLocNumber(String locNumber) {
		this.locNumber = locNumber;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getNoOfAccounts() {
		return noOfAccounts;
	}

	public void setNoOfAccounts(String noOfAccounts) {
		this.noOfAccounts = noOfAccounts;
	}

	public String getClosingBalance() {
		return closingBalance;
	}

	public void setClosingBalance(String closingBalance) {
		this.closingBalance = closingBalance;
	}

	public String getTotalBalance() {
		return totalBalance;
	}

	public void setTotalBalance(String totalBalance) {
		this.totalBalance = totalBalance;
	}
	
	

}
