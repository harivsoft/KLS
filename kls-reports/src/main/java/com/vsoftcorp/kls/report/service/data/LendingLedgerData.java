package com.vsoftcorp.kls.report.service.data;

public class LendingLedgerData {
	
	private Integer pacsId;
	private Integer productId;
	private Integer purposeId;  
	private String borrowingType; 
	private String fromDateString; 
	private String toDateString;
	private String memberId;
	private Integer noOfRecords;
	
	public Integer getPacsId() {
		return pacsId;
	}
	public void setPacsId(Integer pacsId) {
		this.pacsId = pacsId;
	}
	public Integer getProductId() {
		return productId;
	}
	public void setProductId(Integer productId) {
		this.productId = productId;
	}
	public Integer getPurposeId() {
		return purposeId;
	}
	public void setPurposeId(Integer purposeId) {
		this.purposeId = purposeId;
	}
	public String getBorrowingType() {
		return borrowingType;
	}
	public void setBorrowingType(String borrowingType) {
		this.borrowingType = borrowingType;
	}
	public String getFromDateString() {
		return fromDateString;
	}
	public void setFromDateString(String fromDateString) {
		this.fromDateString = fromDateString;
	}
	public String getToDateString() {
		return toDateString;
	}
	public void setToDateString(String toDateString) {
		this.toDateString = toDateString;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public Integer getNoOfRecords() {
		return noOfRecords;
	}
	public void setNoOfRecords(Integer noOfRecords) {
		this.noOfRecords = noOfRecords;
	}
	
	
	

}
