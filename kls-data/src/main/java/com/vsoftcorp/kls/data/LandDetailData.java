package com.vsoftcorp.kls.data;

import java.util.List;

/**
 * This Pojo class is used to map the UI data
 * @author a1605
 *
 */

public class LandDetailData {
	
	private String customerId;
	
	private List<LandData> landRegister;
	
	private List<DeleteLand> deleteLand;
	
	private List<TenantLandDetailsData> tenantLandList;
	
	private List<TenantLandDetailsData> deleteTenantLandList;

	public String getCustId() {
		return customerId;
	}

	public void setCustId(String custId) {
		this.customerId = custId;
	}

	public List<LandData> getLandRegister() {
		return landRegister;
	}

	public void setLandRegister(List<LandData> landRegister) {
		this.landRegister = landRegister;
	}

	public List<DeleteLand> getDeleteLand() {
		return deleteLand;
	}

	public void setDeleteLand(List<DeleteLand> deleteLand) {
		this.deleteLand = deleteLand;
	}

	public List<TenantLandDetailsData> getTenantLandList() {
		return tenantLandList;
	}

	public void setTenantLandList(List<TenantLandDetailsData> tenantLandList) {
		this.tenantLandList = tenantLandList;
	}

	public List<TenantLandDetailsData> getDeleteTenantLandList() {
		return deleteTenantLandList;
	}

	public void setDeleteTenantLandList(
			List<TenantLandDetailsData> deleteTenantLandList) {
		this.deleteTenantLandList = deleteTenantLandList;
	}

}