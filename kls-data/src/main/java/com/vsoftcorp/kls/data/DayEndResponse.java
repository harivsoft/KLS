package com.vsoftcorp.kls.data;

public class DayEndResponse {

	private Integer pacsId;
	private String pacsName;
	private String status;
	private String pacsDate;
	private String remarks;
	
	public Integer getPacsId() {
		return pacsId;
	}
	public void setPacsId(Integer pacsId) {
		this.pacsId = pacsId;
	}
	public String getPacsName() {
		return pacsName;
	}
	public void setPacsName(String pacsName) {
		this.pacsName = pacsName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPacsDate() {
		return pacsDate;
	}
	public void setPacsDate(String pacsDate) {
		this.pacsDate = pacsDate;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
}
