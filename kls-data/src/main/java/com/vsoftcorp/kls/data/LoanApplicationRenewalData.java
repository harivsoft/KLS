package com.vsoftcorp.kls.data;

public class LoanApplicationRenewalData {
	
	private String newFinancialYear;
	
	private String oldFinancialYear;
	
	private UserLoginDetailsData loggedInUserDetails;

	
	

	public String getNewFinancialYear() {
		return newFinancialYear;
	}

	public String getOldFinancialYear() {
		return oldFinancialYear;
	}

	public void setNewFinancialYear(String newFinancialYear) {
		this.newFinancialYear = newFinancialYear;
	}

	public void setOldFinancialYear(String oldFinancialYear) {
		this.oldFinancialYear = oldFinancialYear;
	}

	public UserLoginDetailsData getLoggedInUserDetails() {
		return loggedInUserDetails;
	}

	public void setLoggedInUserDetails(UserLoginDetailsData loggedInUserDetails) {
		this.loggedInUserDetails = loggedInUserDetails;
	}
	
}
