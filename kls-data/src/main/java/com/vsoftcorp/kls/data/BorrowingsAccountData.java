package com.vsoftcorp.kls.data;

/**
 * @author a9153
 * 
 *         This Class is pojo for the Borrowings account entiy.
 * 
 */

public class BorrowingsAccountData {

	public BorrowingsAccountData() {
	}

	private String id;

	private String bankCode;

	private String branchId;

	private String pacsId;
	
	private String productId;

	private String accountNo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getBranchId() {
		return branchId;
	}

	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}

	public String getPacsId() {
		return pacsId;
	}

	public void setPacsId(String pacsId) {
		this.pacsId = pacsId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getAccountNo() {
		return accountNo;
	}

	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

}
