package com.vsoftcorp.kls.data;

/**
 * @author a9153
 * 
 *         This Class is pojo for the Product type entiy.
 * 
 */

public class ProductTypeData {

	private String id;
	private String description;
	private String atmApplicable;
	private String intCategoryId;
	private String loanType;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAtmApplicable() {
		return atmApplicable;
	}

	public void setAtmApplicable(String atmApplicable) {
		this.atmApplicable = atmApplicable;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getIntCategoryId() {
		return intCategoryId;
	}

	public void setIntCategoryId(String intCategoryId) {
		this.intCategoryId = intCategoryId;
	}

	public String getLoanType() {
		return loanType;
	}

	public void setLoanType(String loanType) {
		this.loanType = loanType;
	}

}
