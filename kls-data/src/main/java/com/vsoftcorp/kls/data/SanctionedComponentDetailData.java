package com.vsoftcorp.kls.data;

public class SanctionedComponentDetailData {
	private String id;
	private String componentDescription;
	private String percentageOfKind;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getComponentDescription() {
		return componentDescription;
	}

	public void setComponentDescription(String componentDescription) {
		this.componentDescription = componentDescription;
	}

	public String getPercentageOfKind() {
		return percentageOfKind;
	}

	public void setPercentageOfKind(String percentageOfKind) {
		this.percentageOfKind = percentageOfKind;
	}

}
