package com.vsoftcorp.kls.data;

/**
 * 
 * @author a9152
 * 
 */
public class VillageData {

	private Integer id;

	private String name;

	private Integer talukaId;

	private Integer pin;

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the talukaId
	 */
	public Integer getTalukaId() {
		return talukaId;
	}

	/**
	 * @param talukaId
	 *            the talukaId to set
	 */
	public void setTalukaId(Integer talukaId) {
		this.talukaId = talukaId;
	}

	public Integer getPin() {
		return pin;
	}

	public void setPin(Integer pin) {
		this.pin = pin;
	}

}
