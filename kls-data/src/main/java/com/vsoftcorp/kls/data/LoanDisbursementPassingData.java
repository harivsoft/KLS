package com.vsoftcorp.kls.data;

public class LoanDisbursementPassingData {
	
	private String memberNumber;
	
	private String memberName;
	
	private String sanctionedLimit;
	
	private String disbursementAmount;
	
	private String disbursementDate;
	
	private String locId;

	public String getMemberNumber() {
		return memberNumber;
	}

	public void setMemberNumber(String memberNumber) {
		this.memberNumber = memberNumber;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getSanctionedLimit() {
		return sanctionedLimit;
	}

	public void setSanctionedLimit(String sanctionedLimit) {
		this.sanctionedLimit = sanctionedLimit;
	}

	public String getDisbursementAmount() {
		return disbursementAmount;
	}

	public void setDisbursementAmount(String disbursementAmount) {
		this.disbursementAmount = disbursementAmount;
	}

	public String getDisbursementDate() {
		return disbursementDate;
	}

	public void setDisbursementDate(String disbursementDate) {
		this.disbursementDate = disbursementDate;
	}

	public String getLocId() {
		return locId;
	}

	public void setLocId(String locId) {
		this.locId = locId;
	}
	
	

}
