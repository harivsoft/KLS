package com.vsoftcorp.kls.data;

public class LoggedInUserDetailsData {

	private String businessDate;
	
	private String branchId;
	
	private String pacsId;
	
	private String userId;

	private Boolean isBankUser;
	
	private String bankCode;
	
	private String pacsUserType;
	/**
	 * @return the businessDate
	 */
	public String getBusinessDate() {
		return businessDate;
	}

	/**
	 * @param businessDate the businessDate to set
	 */
	public void setBusinessDate(String businessDate) {
		this.businessDate = businessDate;
	}

	/**
	 * @return the branchId
	 */
	public String getBranchId() {
		return branchId;
	}

	/**
	 * @param branchId the branchId to set
	 */
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}

	/**
	 * @return the pacsId
	 */
	public String getPacsId() {
		return pacsId;
	}

	/**
	 * @param pacsId the pacsId to set
	 */
	public void setPacsId(String pacsId) {
		this.pacsId = pacsId;
	}

	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Boolean getIsBankUser() {
		return isBankUser;
	}

	public void setIsBankUser(Boolean isBankUser) {
		this.isBankUser = isBankUser;
	}

	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	public String getPacsUserType() {
		return pacsUserType;
	}

	public void setPacsUserType(String pacsUserType) {
		this.pacsUserType = pacsUserType;
	}
	
}
