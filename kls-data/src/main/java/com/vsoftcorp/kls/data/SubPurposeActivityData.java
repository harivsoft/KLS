package com.vsoftcorp.kls.data;

public class SubPurposeActivityData {

	private String id;
	private Integer subPurposeId;
	private String subPurposeName;
	private Integer activityId;
	private String activityName;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getSubPurposeId() {
		return subPurposeId;
	}

	public void setSubPurposeId(Integer subPurposeId) {
		this.subPurposeId = subPurposeId;
	}

	public String getSubPurposeName() {
		return subPurposeName;
	}

	public void setSubPurposeName(String subPurposeName) {
		this.subPurposeName = subPurposeName;
	}

	public Integer getActivityId() {
		return activityId;
	}

	public void setActivityId(Integer activityId) {
		this.activityId = activityId;
	}

	public String getActivityName() {
		return activityName;
	}

	public void setActivityName(String activityName) {
		this.activityName = activityName;
	}
}
